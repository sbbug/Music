package com.music.java.vo;

import java.util.Date;

public class Singer {

	private int singer_id;
	private String singer_name;
	private Date singer_birthday;
	private String singer_zodiac;
	private String singer_constellation;
	private String singer_hobby;
	private String singer_brief_intro;
	
	public int getSinger_id() {
		return singer_id;
	}
	public void setSinger_id(int singer_id) {
		this.singer_id = singer_id;
	}
	public String getSinger_name() {
		return singer_name;
	}
	public void setSinger_name(String singer_name) {
		this.singer_name = singer_name;
	}
	public Date getSinger_birthday() {
		return singer_birthday;
	}
	public void setSinger_birthday(Date singer_birthday) {
		this.singer_birthday = singer_birthday;
	}
	public String getSinger_zodiac() {
		return singer_zodiac;
	}
	public void setSinger_zodiac(String singer_zodiac) {
		this.singer_zodiac = singer_zodiac;
	}
	public String getSinger_constellation() {
		return singer_constellation;
	}
	public void setSinger_constellation(String singer_constellation) {
		this.singer_constellation = singer_constellation;
	}
	public String getSinger_hobby() {
		return singer_hobby;
	}
	public void setSinger_hobby(String singer_hobby) {
		this.singer_hobby = singer_hobby;
	}
	public String getSinger_brief_intro() {
		return singer_brief_intro;
	}
	public void setSinger_brief_intro(String singer_brief_intro) {
		this.singer_brief_intro = singer_brief_intro;
	}
	
	
	
	
}
